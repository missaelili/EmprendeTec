            <div class="title-box">
              <h2 class="title-b">¿Qué es EmprendeTEC?</h2>
            </div>

            <p class="post-intro">
            EmprendeTEC es una aplicación Web que permite dar a conocer la creatividad de los estudiantes, académicos y talento local mediante la publicación de sus proyectos de inversión o innovadores con la finalidad de obtener recursos para llevarlos a cabo, utilizando una interfaz sencilla y atractiva con una asociación civil como intermediario entre los fondos recaudados y los diferentes proyectos. 
Esta plataforma permitirá, además, generar una bolsa de trabajo para complementar proyectos que se logren aterrizar, dando así más oportunidades de empleo.
            </p>

            <div class="title-box">
              <h2 class="title-b">¿Cómo puedo apoyar a los Emprendedores?</h2>
            </div>

            <p class="post-intro">
            Para apoyar un proyecto se utilizara la metodología crowfounding donde existen dos tipos de apoyo, el primero, apoyo no financiero que será una donación al emprendedor que no necesitara de iniciar sesión ni otra cosa más que querer apoyar un proyecto, el segundo, apoyo financiado en el que se podrá elegir entre los tipos de apoyos que se le puede dar a cada uno dependiendo de sus necesidades. Para poder aportar en este tipo de financiamiento es necesario de crear una cuenta. 
Si un proyecto al que se apoyó no logra llegar a su meta de financiamiento el dinero propuesto para este se destinara a otro similar o de la misma categoría.
            </p>

            <div class="title-box">
              <h2 class="title-b">¿Qué y a quienes apoya el EmprendeTEC?</h2>
            </div>

            <p class="post-intro">
         
 El objetivo principal de EmprendeTEC es apoyar las nuevas ideas y proyectos innovadores propuestos por los alumnos de la institución que han llegado a un nivel económicamente factible tanto acreedores al pase nacional del eneit como proyectos incubados en alguna de las incubadoras de los diferentes planteles del TEC NM.
Los recursos recabados serán administrados por una sociedad civil creada en forma bipartita por los empresarios o consejo de vinculación y por el TEC NM que harán la principal aportación al desarrollo de proyectos que han sido valorados mediante concurso y/o incubadora mediante procesos claros y transparentes.
Los requisitos para que un proyecto sea aceptado son:
<br>
•	Haber emanado de una etapa nacional del concurso eneit organizado por TEC NM o de cualquier incubadora perteneciente a este sistema.
<br>
•	Demostrar la rentabilidad del proyecto a un plazo máximo de 5 años.
<br>
•	Haber registrado la patente o marca del proyecto o idea.

            </p>
           
